package it.deltascientia.model;

import jakarta.persistence.*;
import lombok.*;
import org.hibernate.annotations.CreationTimestamp;
import org.hibernate.annotations.UpdateTimestamp;

import java.time.Instant;
import java.util.ArrayList;
import java.util.List;

/**
 * Root entity representing a scientific experiment.
 * An experiment contains zero or more {@link Variable} definitions and
 * zero or more {@link Trial} runs.
 */
@Entity
@Table(name = "experiment")
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class Experiment {

    /** Unique identifier, auto-generated by the database. */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @EqualsAndHashCode.Include
    private Long id;

    /** Human-readable experiment name. Must not be blank. */
    @Column(nullable = false, length = 255)
    private String name;

    /** Optional description of the experiment purpose and scope. */
    @Column(length = 1000)
    private String description;

    /** Categorisation tag for grouping experiments (e.g. "thermodynamics"). */
    @Column(length = 100)
    private String category;

    /** Current lifecycle state (e.g. "DRAFT", "ACTIVE", "COMPLETED"). */
    @Column(length = 50)
    private String status;

    /** Username or identifier of the person who created the experiment. */
    @Column(name = "created_by", length = 255)
    private String createdBy;

    /** Free-form notes about the experiment. */
    @Lob
    @Column(columnDefinition = "TEXT")
    private String notes;

    /** Timestamp of row creation, managed by Hibernate. */
    @CreationTimestamp
    @Column(name = "created_at", nullable = false, updatable = false)
    private Instant createdAt;

    /** Timestamp of last update, managed by Hibernate. */
    @UpdateTimestamp
    @Column(name = "updated_at")
    private Instant updatedAt;

    /** Trials belonging to this experiment. Cascade persists them together. */
    @OneToMany(mappedBy = "experiment", cascade = CascadeType.ALL, orphanRemoval = true)
    @Builder.Default
    private List<Trial> trials = new ArrayList<>();

    /** Variable definitions shared across all trials in this experiment. */
    @OneToMany(mappedBy = "experiment", cascade = CascadeType.ALL, orphanRemoval = true)
    @Builder.Default
    private List<Variable> variables = new ArrayList<>();
}
